import math

Val = math.pi
sqrtVal = math.sqrt(Val)
print(sqrtVal)

# var*int -> multiplication
# var**int -> squaring

expVal = math.exp(Val)
print(expVal)

#factVal = math.factorial(Val)
#print(factVal)

sinVal = math.sin(Val)
print(sinVal)

floorVal = math.floor(Val)
print(floorVal)

ceilVal = math.ceil(Val)
print(ceilVal)

